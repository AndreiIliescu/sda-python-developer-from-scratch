# Function definition with the name print_hello_world
def print_hello_world():
    print("Welcome to the world inside a function!")


# Function call print_hello_world()
print_hello_world()


# Function definition greet_by_name(name)
def greet_by_name(name):
    print(f"Hi, {name}")


# Function call greet_by_name(name) use the value "John" as the argument name
greet_by_name("John")
greet_by_name("Mihai")
greet_by_name("Popa")
